						<p>“我们去年一亿多人次出国旅游，结果出国漫游的增长速度却是下降的，因为漫游费太贵了!我听说，很多导游都随身带一个wifi信号发射器，既方便组织游客，又为他们省了钱。”在研究讨论降低网费、流量费时李克强说，“一亿游客出国，这是多大的市场啊!老百姓很清楚，你的网费、流量费太高，他就不用了!”</p>

