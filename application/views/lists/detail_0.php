						<p>加快信息基础设施建设，进一步提高网速与“降网费”直接相关。“中国是世界上第一大手机拥有国，但网速在世界仅排名80多位，信息基础设施建设确实太滞后了。”李克强说。</p>

						<p>他解释说，当前加快推进信息基础设施建设，一方面可以拉动投资、促进稳增长，另一方面也为发展“互联网+”、推进大众创业万众创新提供了有力的支撑。</p>

						<p>李克强最后强调，提网速、降网费，不仅需要工信部和通信运营商的不懈努力，还需要有关部门的通力合作，为电信企业提供良好的营商环境，最终便利亿万网民。</p>
